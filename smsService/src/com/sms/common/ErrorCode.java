package com.sms.common;

/**
 * 错误码定义
 * @author douk
 *
 */
public class ErrorCode {
	
	/**
	 * 成功
	 */
	public static final int SUCCESS = 100;
	/**
	 * 应用编码无效
	 */
	public static final int KEYNOT = 101;
	/**
	 * 发送内容不能为空
	 */
	public static final int OBJNULL = 102;
	/**
	 * 手机号码不能为空
	 */
	public static final int ERRNULL = 103;
	/**
	 * 数据格式异常
	 */
	public static final int ERRDATA = 104;
	/**
	 * 短信验证码格式不正确
	 */
	public static final int ERRCODE = 105;
	/**
	 * 短信验证码已过期
	 */
	public static final int ERRCODEGQ = 106;
	/**
	 * 短信验证码失败
	 */
	public static final int ERRVCODEGQ = 107;
	/**
	 * 发送失败
	 */
	public static final int FAILURE = 200;
	
	/**
	 * 订单编号为空
	 */
	public static final int BIDNULL = 201;
	
	/**
	 * 商户名称或商品名称为空
	 */
	public static final int SELLERNULL = 202;
	
	/**
	 * 微信openid为空
	 */
	public static final int OPENNULL = 203;
	
	/**
	 * 订单支付金额为空
	 */
	public static final int MONEYNULL = 204;
	
	/**
	 * 通知标题为空
	 */
	public static final int TITLENULL = 205;
}
