package com.xmniao.common;

/**
 * 调用请求退款业务接口返回状态码
 * @author ChenBo
 *
 */
public class StateCodeUtil {

	//退款成功
	public static final String PR_SUCCESS = "30000";

	//参数错误
	public static final String PR_PARAM_ERROR = "30001";

	//已退款，属重复退款
	public static final String PR_REFUNDED = "30002";
	
	//钱包状态异常
	public static final String PR_WALLET_ERROR = "30003";

	//订单不存在
	public static final String PR_ORDER_NOT_EXIST = "30004";

	//订单没完成付款
	public static final String PR_ORDER_NOTPAY = "30005";
	
	//订单不允许退款
	public static final String PR_ORDER_DISENABLE = "30006";

	//退款记录表中无该订单记录
	public static final String PR_NOREFUND_RECORE = "30007";

	//系统错误
	public static final String PR_SYSTEM_ERROR = "30008";
	
	//退款总金额错误
	public static final String PR_MONEY_ERROR = "30009";
	
	//请求第三方支付数据组合 成功
	public static final String PR_REQUEST_THIRD_SUCCESS = "30010";
	
	//正在进行退款处理，属重复退款
	public static final String PR_REQUEST_REPEAT = "30011";
	
	//不支持的支付方式类型
	public static final String PR_NOT_SUPPORTTYPE_PAY = "30012";
	
	//第三方服务器反馈退款退款失败
	public static final String PR_THIRD_SERVER_FAIL = "30013";
	
	//第三方服务器反馈未确定状态，需重新退款
	public static final String PR_THIRD_SERVER_UNKOWN = "30014";
	
	//第三方服务器反馈已退款，但退款目标账号异常，转退至平台
	public static final String PR_REFUND_TO_XMN = "30015";
	
	//第三方服务器连接失败
	public static final String PR_THIRD_NOT_CONNECT = "30016";
	
	//支付订单或交易流水订单异常
	public static final String PR_ORDER_EXCEPTION ="30017";
	
	//调用业务服务失败
	public static final String PR_SUSINE_SERVICE_ERROR = "30018";
	
	//已提交退款请求，第三方平台还在退款处理中
	public static final String PR_THIRD_REFUNDING = "30019";
	
	//其他原因退款失败
	public static final String PR_REFUND_FAIL ="30020";
}
