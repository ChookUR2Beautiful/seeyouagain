package com.xmniao.enums;

/**
 * 道具操作类型
 *
 * @author liyuanbo
 * @create 2017-05-31 16:57
 **/
public enum PropsOperationTypeEnum {
    ADD(1, "增加"),
    MIUNS(2, "减少");
    private int type;
    private String remark;

    private PropsOperationTypeEnum(int type, String remark) {
        this.type = type;
        this.remark = remark;
    }

    public int getType() {
        return type;
    }

    public String getRemark() {
        return remark;
    }

    public void setType(int type) {
        this.type = type;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}
