public class SunProfit {
    private long id ;
    private int baseNumber; //基础阳光数量
    private double profit; //利率比例

    public long getId() {
        return id;
    }

    public int getBaseNumber() {
        return baseNumber;
    }

    public double getProfit() {
        return profit;
    }

    public void setId(long id) {
        this.id = id;
    }

    public void setBaseNumber(int baseNumber) {
        this.baseNumber = baseNumber;
    }

    public void setProfit(double profit) {
        this.profit = profit;
    }

    @Override
    public String toString() {
        return "SunProfit{" +
                "id=" + id +
                ", baseNumber=" + baseNumber +
                ", profit=" + profit +
                '}';
    }
}
