package com.xmniao.entity.manor;

/**
 * 获取用户账单
 *
 * @author liyuanbo
 * @create 2017-06-15 16:36
 **/
public class PropsReport {

    private String transNo;//交易号
    private String number;//操作的数量
    private int type;//操作类型
    private int channel;//操作渠道
    private String createTime;//创建时间
    private long commonUid;
    private double commonNumber;

    public String getTransNo() {
        return transNo;
    }

    public String getNumber() {
        return number;
    }

    public int getType() {
        return type;
    }

    public int getChannel() {
        return channel;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setTransNo(String transNo) {
        this.transNo = transNo;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public void setType(int type) {
        this.type = type;
    }

    public void setChannel(int channel) {
        this.channel = channel;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public long getCommonUid() {
        return commonUid;
    }

    public double getCommonNumber() {
        return commonNumber;
    }

    public void setCommonUid(long commonUid) {
        this.commonUid = commonUid;
    }

    public void setCommonNumber(double commonNumber) {
        this.commonNumber = commonNumber;
    }

    @Override
    public String toString() {
        return "PropsReport{" +
                "transNo='" + transNo + '\'' +
                ", number='" + number + '\'' +
                ", type=" + type +
                ", channel=" + channel +
                ", createTime='" + createTime + '\'' +
                ", commonUid=" + commonUid +
                ", commonNumber=" + commonNumber +
                '}';
    }
}
