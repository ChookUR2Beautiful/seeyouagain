package com.xmniao.entity.manor;

/**
 * 道具红包赠送记录表
 *
 * @author liyuanbo
 * @create 2017-08-01 15:22
 **/
public class PropsRedpackageGiveRecord {

    private long id;
    private long redpackageId;
    private long giveUid;
    private double amount;
    private int status = 1;//1未领取 2.已领取
    private String createTime;//创建时间
    private String updateTime;//修改时间

    public long getId() {
        return id;
    }

    public long getRedpackageId() {
        return redpackageId;
    }

    public long getGiveUid() {
        return giveUid;
    }

    public double getAmount() {
        return amount;
    }

    public int getStatus() {
        return status;
    }

    public void setId(long id) {
        this.id = id;
    }

    public void setRedpackageId(long redpackageId) {
        this.redpackageId = redpackageId;
    }

    public void setGiveUid(long giveUid) {
        this.giveUid = giveUid;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getCreateTime() {
        return createTime;
    }

    public String getUpdateTime() {
        return updateTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public void setUpdateTime(String updateTime) {
        this.updateTime = updateTime;
    }

    @Override
    public String toString() {
        return "PropsRedpackageGiveRecord{" +
                "id=" + id +
                ", redpackageId=" + redpackageId +
                ", giveUid=" + giveUid +
                ", amount=" + amount +
                ", status=" + status +
                ", createTime='" + createTime + '\'' +
                ", updateTime='" + updateTime + '\'' +
                '}';
    }
}
