package com.xmniao.entity.manor;

/**
 * 花操作记录表
 * @author liyuanbo
 * @create 2017-07-06 10:32
 **/
public class FlowerRecord {
    private long id ;
    private long uid; //用户ID
    private String transNo;//交易号ID
    private double number;//操作数量
    private int propsType;//1.花朵 2.普通能量 3.黄金能量
    private int monthType ;//花朵月份类型
    private String createTime ;// 创建时间
    private String updateTime ;//更新时间

    public long getId() {
        return id;
    }

    public long getUid() {
        return uid;
    }

    public double getNumber() {
        return number;
    }

    public int getPropsType() {
        return propsType;
    }

    public int getMonthType() {
        return monthType;
    }

    public String getCreateTime() {
        return createTime;
    }

    public String getUpdateTime() {
        return updateTime;
    }

    public void setId(long id) {
        this.id = id;
    }

    public void setUid(long uid) {
        this.uid = uid;
    }

    public void setNumber(double number) {
        this.number = number;
    }

    public void setPropsType(int propsType) {
        this.propsType = propsType;
    }

    public void setMonthType(int monthType) {
        this.monthType = monthType;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public void setUpdateTime(String updateTime) {
        this.updateTime = updateTime;
    }

    public String getTransNo() {
        return transNo;
    }

    public void setTransNo(String transNo) {
        this.transNo = transNo;
    }

    @Override
    public String toString() {
        return "FlowerRecord{" +
                "id=" + id +
                ", uid=" + uid +
                ", number=" + number +
                ", propsType=" + propsType +
                ", monthType=" + monthType +
                ", createTime='" + createTime + '\'' +
                ", updateTime='" + updateTime + '\'' +
                '}';
    }
}
