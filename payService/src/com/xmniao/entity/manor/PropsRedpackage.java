package com.xmniao.entity.manor;

/**
 * 道具红包发放记录
 * @author liyuanbo
 * @create 2017-06-03 13:51
 **/
public class PropsRedpackage {
    private long id;
    private String transNo;//消费交易号
    private long uid;//用户ID
    private int propsType;//道具类型
    private int type ;//红包类型  1.随机 2.固定 3.指定人
    private int number ;// 红包数量
    private double singleRedpackageAmount;//单个红包额度
    private int status ;//红包状态 1.未领完 2.已领完 3.已退回
    private int currentGetNumber ; //当前已经领取的红包个数
    private long giveUid ;//赠送人UID
    private double serviceChargeProfit;//手续费比例
    private double amount;//当红包类型为随机的时候，填写总金额
    private String phone ;//当前发送红包的手机号。用于退回红包发送短信

    private String createTime ; //创建时间
    private String updateTime ;//更新时间
    private String exprieTime ; //过期时间

    public long getId() {
        return id;
    }

    public String getTransNo() {
        return transNo;
    }

    public long getUid() {
        return uid;
    }

    public int getPropsType() {
        return propsType;
    }

    public int getType() {
        return type;
    }

    public int getNumber() {
        return number;
    }



    public int getStatus() {
        return status;
    }

    public int getCurrentGetNumber() {
        return currentGetNumber;
    }

    public String getCreateTime() {
        return createTime;
    }

    public String getUpdateTime() {
        return updateTime;
    }

    public String getExprieTime() {
        return exprieTime;
    }

    public void setId(long id) {
        this.id = id;
    }

    public void setTransNo(String transNo) {
        this.transNo = transNo;
    }

    public void setUid(long uid) {
        this.uid = uid;
    }

    public void setPropsType(int propsType) {
        this.propsType = propsType;
    }

    public void setType(int type) {
        this.type = type;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public double getSingleRedpackageAmount() {
        return singleRedpackageAmount;
    }

    public void setSingleRedpackageAmount(double singleRedpackageAmount) {
        this.singleRedpackageAmount = singleRedpackageAmount;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public void setCurrentGetNumber(int currentGetNumber) {
        this.currentGetNumber = currentGetNumber;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public void setUpdateTime(String updateTime) {
        this.updateTime = updateTime;
    }

    public void setExprieTime(String exprieTime) {
        this.exprieTime = exprieTime;
    }

    public long getGiveUid() {
        return giveUid;
    }

    public void setGiveUid(long giveUid) {
        this.giveUid = giveUid;
    }

    public double getServiceChargeProfit() {
        return serviceChargeProfit;
    }

    public void setServiceChargeProfit(double serviceChargeProfit) {
        this.serviceChargeProfit = serviceChargeProfit;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    @Override
    public String toString() {
        return "PropsRedpackage{" +
                "id=" + id +
                ", transNo='" + transNo + '\'' +
                ", uid=" + uid +
                ", propsType=" + propsType +
                ", type=" + type +
                ", number=" + number +
                ", singleRedpackageAmount=" + singleRedpackageAmount +
                ", status=" + status +
                ", currentGetNumber=" + currentGetNumber +
                ", giveUid=" + giveUid +
                ", serviceChargeProfit=" + serviceChargeProfit +
                ", amount=" + amount +
                ", phone='" + phone + '\'' +
                ", createTime='" + createTime + '\'' +
                ", updateTime='" + updateTime + '\'' +
                ", exprieTime='" + exprieTime + '\'' +
                '}';
    }
}
