package com.xmniao.entity;

/**
 * 提现配置信息
 * @author zenghm
 *
 */
public class WithdrawMoneyConfig {

	/** 最低提现金额  目前只用于商家  */
	private double minMoney = 50.00;
	/** 最高提现金额  */
	private double maxMoney = 50000.00;
	/** 合作商提现是否有限制  */
	private boolean limitJoint = true;
	/** 商家提现是否有限制  */
	private boolean limitSeller = true;
	/** 用户提现是否有限制  */
	private boolean limitUser = true;

	public double getMinMoney() {
		return minMoney;
	}

	public void setMinMoney(double minMoney) {
		this.minMoney = minMoney;
	}

	public double getMaxMoney() {
		return maxMoney;
	}

	public void setMaxMoney(double maxMoney) {
		this.maxMoney = maxMoney;
	}

	public boolean isLimitJoint() {
		return limitJoint;
	}

	public void setLimitJoint(boolean limitJoint) {
		this.limitJoint = limitJoint;
	}

	public boolean isLimitSeller() {
		return limitSeller;
	}

	public void setLimitSeller(boolean limitSeller) {
		this.limitSeller = limitSeller;
	}

	public boolean isLimitUser() {
		return limitUser;
	}

	public void setLimitUser(boolean limitUser) {
		this.limitUser = limitUser;
	}

	
}
