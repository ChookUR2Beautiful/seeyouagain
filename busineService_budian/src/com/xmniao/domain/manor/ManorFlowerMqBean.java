package com.xmniao.domain.manor;

import java.util.List;

/**
 * Created by yang.qiang on 2017/7/13.
 * MQ 消息传输封装Bean
 */
public class ManorFlowerMqBean {
    private Integer uid;        // 用户uid
    private Integer subUid;     // 下级用户uid
    private Integer parentUid;  // 上级用户uid

    private Integer Location;   // 花朵位置 0 1 2
    private Integer type;       // 花朵类型
    private ManorFlower flower;     // 花朵参数
    private List<ManorFlower> flowers;  // 花朵列表

    private String transNo;      // 交易号

    public String getTransNo() {
        return transNo;
    }

    public void setTransNo(String transNo) {
        this.transNo = transNo;
    }

    public Integer getUid() {
        return uid;
    }

    public void setUid(Integer uid) {
        this.uid = uid;
    }

    public Integer getSubUid() {
        return subUid;
    }

    public void setSubUid(Integer subUid) {
        this.subUid = subUid;
    }

    public Integer getParentUid() {
        return parentUid;
    }

    public void setParentUid(Integer parentUid) {
        this.parentUid = parentUid;
    }

    public Integer getLocation() {
        return Location;
    }

    public void setLocation(Integer location) {
        Location = location;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public ManorFlower getFlower() {
        return flower;
    }

    public void setFlower(ManorFlower flower) {
        this.flower = flower;
    }

    public List<ManorFlower> getFlowers() {
        return flowers;
    }

    public void setFlowers(List<ManorFlower> flowers) {
        this.flowers = flowers;
    }
}
