public class FansConfigureRequest {
	
	private String ids;//主播id，多个以","分隔
	
	private String uids;//主播UID，多个以","分隔
	
	private Integer fansMin;//最小粉丝数
	
	private Integer fansMax;//最大粉丝数
	
	
	

	/**
	 * @return the ids
	 */
	public String getIds() {
		return ids;
	}

	/**
	 * @param ids the ids to set
	 */
	public void setIds(String ids) {
		this.ids = ids;
	}

	/**
	 * @return the uids
	 */
	public String getUids() {
		return uids;
	}

	/**
	 * @param uids the uids to set
	 */
	public void setUids(String uids) {
		this.uids = uids;
	}

	/**
	 * @return the fansMin
	 */
	public Integer getFansMin() {
		return fansMin;
	}

	/**
	 * @param fansMin the fansMin to set
	 */
	public void setFansMin(Integer fansMin) {
		this.fansMin = fansMin;
	}
	
	


	/**
	 * @return the fansMax
	 */
	public Integer getFansMax() {
		return fansMax;
	}

	/**
	 * @param fansMax the fansMax to set
	 */
	public void setFansMax(Integer fansMax) {
		this.fansMax = fansMax;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "FansConfigureRequest [ids=" + ids + ", uids=" + uids
				+ ", fansMin=" + fansMin + ", fansMax=" + fansMax + "]";
	}

	
	

}
