public class TPicType extends BaseEntity {

	private static final long serialVersionUID = 5388129872163819932L;
	private Integer pid;// 类型ID
	private Integer tid;// 行业类别ID
	private String name;// 类型名称
	private Date sdate;// 更新时间

	/**
	 * 创建一个新的实例 TPicType.
	 * 
	 */
	public TPicType() {
		super();
	}

	public TPicType(Integer pid) {
		this.pid = pid;
	}

	/**
	 * pid
	 * 
	 * @return the pid
	 */
	public Integer getPid() {
		return pid;
	}

	/**
	 * @param pid
	 *            the pid to set
	 */
	public void setPid(Integer pid) {
		this.pid = pid;
	}

	/**
	 * tid
	 * 
	 * @return the tid
	 */
	public Integer getTid() {
		return tid;
	}

	/**
	 * @param tid
	 *            the tid to set
	 */
	public void setTid(Integer tid) {
		this.tid = tid;
	}

	/**
	 * name
	 * 
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * sdate
	 * 
	 * @return the sdate
	 */
	public Date getSdate() {
		return sdate;
	}

	/**
	 * @param sdate
	 *            the sdate to set
	 */
	public void setSdate(Date sdate) {
		this.sdate = sdate;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "TPicType [pid=" + pid + ", tid=" + tid + ", name=" + name + ", sdate=" + sdate + ", ]";
	}
}
