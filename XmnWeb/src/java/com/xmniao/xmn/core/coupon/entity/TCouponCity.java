public class TCouponCity extends BaseEntity {

	private static final long serialVersionUID = -2035544965499541037L;

	private Integer ccid;//
	private Integer cid;//优惠券id
	private Integer provinces;//显示省份
	private Integer city;//显示城市
	
	public Integer getCcid() {
		return ccid;
	}
	public void setCcid(Integer ccid) {
		this.ccid = ccid;
	}
	public Integer getCid() {
		return cid;
	}
	public void setCid(Integer cid) {
		this.cid = cid;
	}
	public Integer getProvinces() {
		return provinces;
	}
	public void setProvinces(Integer provinces) {
		this.provinces = provinces;
	}
	public Integer getCity() {
		return city;
	}
	public void setCity(Integer city) {
		this.city = city;
	}
	@Override
	public String toString() {
		return "TCouponCity [ccid=" + ccid + ", cid=" + cid + ", provinces="
				+ provinces + ", city=" + city + "]";
	}
}
