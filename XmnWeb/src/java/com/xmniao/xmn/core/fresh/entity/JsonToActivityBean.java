public class JsonToActivityBean {
	private Integer sort;
	private String pvIds;
	private Integer stock;
	private BigDecimal amount;
	private Long codeId;
	private List<String> pvValues;
	private Integer maxStock;
	public Integer getMaxStock() {
		return maxStock;
	}
	public void setMaxStock(Integer maxStock) {
		this.maxStock = maxStock;
	}
	public Integer getSort() {
		return sort;
	}
	public void setSort(Integer sort) {
		this.sort = sort;
	}
	public String getPvIds() {
		return pvIds;
	}
	public void setPvIds(String pvIds) {
		this.pvIds = pvIds;
	}
	public Integer getStock() {
		return stock;
	}
	public void setStock(Integer stock) {
		this.stock = stock;
	}
	public BigDecimal getAmount() {
		return amount;
	}
	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}
	public Long getCodeId() {
		return codeId;
	}
	public void setCodeId(Long codeId) {
		this.codeId = codeId;
	}
	public List<String> getPvValues() {
		return pvValues;
	}
	public void setPvValues(List<String> pvValues) {
		this.pvValues = pvValues;
	}
	
}
