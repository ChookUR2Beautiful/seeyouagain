public class JsonToGroupBean<T> {
	private List<T> saleGroupList;
	
	public List<T> getSaleGroupList() {
		return saleGroupList;
	}

	public void setSaleGroupList(List<T> saleGroupList) {
		this.saleGroupList = saleGroupList;
	}

	private BigDecimal cash;
	
	private BigDecimal integral;
	
	private String codeId;
	
	private Integer sort;
	
	private String type;
	
	private Integer maxStore;
	
	private Integer store;
	
	public Integer getStore() {
		return store;
	}

	public void setStore(Integer store) {
		this.store = store;
	}

	public Integer getMaxStore() {
		return maxStore;
	}

	public void setMaxStore(Integer maxStore) {
		this.maxStore = maxStore;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Integer getSort() {
		return sort;
	}

	public void setSort(Integer sort) {
		this.sort = sort;
	}

	public BigDecimal getCash() {
		return cash;
	}

	public void setCash(BigDecimal cash) {
		this.cash = cash;
	}

	public BigDecimal getIntegral() {
		return integral;
	}

	public void setIntegral(BigDecimal integral) {
		this.integral = integral;
	}

	public String getCodeId() {
		return codeId;
	}

	public void setCodeId(String codeId) {
		this.codeId = codeId;
	}
	
	
	
}
