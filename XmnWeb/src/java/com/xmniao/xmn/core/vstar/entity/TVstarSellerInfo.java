public class TVstarSellerInfo extends BaseEntity {

	/**
	 * 
	 */
	private static final long serialVersionUID = 8595311949071838788L;
	
	private Integer sellerid;//商家ID
	
	private String sellername;//商家名称
	
	private String fullname;//负责人
	
	private String phoneid;//负责人手机号
	
	private Integer uid;//签店人UID
	
	private BigDecimal moneySum;//流水累计收益
	
	private BigDecimal mikeAmountSum;//寻蜜客获得商户收益

	/**
	 * @return the sellerid
	 */
	public Integer getSellerid() {
		return sellerid;
	}

	/**
	 * @param sellerid the sellerid to set
	 */
	public void setSellerid(Integer sellerid) {
		this.sellerid = sellerid;
	}

	/**
	 * @return the sellername
	 */
	public String getSellername() {
		return sellername;
	}

	/**
	 * @param sellername the sellername to set
	 */
	public void setSellername(String sellername) {
		this.sellername = sellername;
	}

	/**
	 * @return the fullname
	 */
	public String getFullname() {
		return fullname;
	}

	/**
	 * @param fullname the fullname to set
	 */
	public void setFullname(String fullname) {
		this.fullname = fullname;
	}

	/**
	 * @return the phoneid
	 */
	public String getPhoneid() {
		return phoneid;
	}

	/**
	 * @param phoneid the phoneid to set
	 */
	public void setPhoneid(String phoneid) {
		this.phoneid = phoneid;
	}

	/**
	 * @return the uid
	 */
	public Integer getUid() {
		return uid;
	}

	/**
	 * @param uid the uid to set
	 */
	public void setUid(Integer uid) {
		this.uid = uid;
	}
	
	

	/**
	 * @return the moneySum
	 */
	public BigDecimal getMoneySum() {
		return moneySum;
	}

	/**
	 * @param moneySum the moneySum to set
	 */
	public void setMoneySum(BigDecimal moneySum) {
		this.moneySum = moneySum;
	}

	/**
	 * @return the mikeAmountSum
	 */
	public BigDecimal getMikeAmountSum() {
		return mikeAmountSum;
	}

	/**
	 * @param mikeAmountSum the mikeAmountSum to set
	 */
	public void setMikeAmountSum(BigDecimal mikeAmountSum) {
		this.mikeAmountSum = mikeAmountSum;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "TVstarSellerInfo [sellerid=" + sellerid + ", sellername="
				+ sellername + ", fullname=" + fullname + ", phoneid="
				+ phoneid + ", uid=" + uid + ", moneySum=" + moneySum
				+ ", mikeAmountSum=" + mikeAmountSum + "]";
	}
	
	
	

}
