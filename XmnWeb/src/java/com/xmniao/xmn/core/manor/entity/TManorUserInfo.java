package com.xmniao.xmn.core.manor.entity;


import javax.persistence.Table;

import com.xmniao.xmn.core.base.BaseEntity;

/*庄园会员管理*/
@Table(name="b_manor_info")
public class TManorUserInfo extends BaseEntity{
    /**
	 * 
	 */
	private static final long serialVersionUID = -7952841398935072121L;

    /*自定义字段专区*/
	
    /**
	 * 
	 */
	private String name;//真实姓名

    private String nickname;//用户昵称

    private String phone;//手机号码 
    
    private String levelName;//级别名称
    
    public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getLevelName() {
		return levelName;
	}

	public void setLevelName(String levelName) {
		this.levelName = levelName;
	}
	
	
    /* **************自定义字段专区*** */
    
}