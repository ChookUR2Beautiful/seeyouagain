package com.xmniao.xmn.core.service_management.entity;

import com.xmniao.xmn.core.base.BaseEntity;

public class PhoneRecord extends BaseEntity {
	/**
	 * 
	 */
	private static final long serialVersionUID = 8740730866782435525L;
	private long id; //记录ID
	private long bid; //订单编号
	private String name;  //对应用户名称
	private String phone; //手机号码
	private int issueType; //0 订单问题  1 咨询类  2 投诉  3 其他问题
	private int userType; //0 会员用户  1 向蜜客  2 商家  3合作商  4非注册会员
	private String issueRecord; //客服问题记录
	private int result;//0 待处理  1 已处理
	private int isSend;//0 否  1 是
	private String dateCreate;//电话呼入时间
	private String remark;//remark
	
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getBid() {
		return bid;
	}
	public void setBid(long bid) {
		this.bid = bid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public int getIssueType() {
		return issueType;
	}
	public void setIssueType(int issueType) {
		this.issueType = issueType;
	}
	public int getUserType() {
		return userType;
	}
	public void setUserType(int userType) {
		this.userType = userType;
	}
	public String getIssueRecord() {
		return issueRecord;
	}
	public void setIssueRecord(String issueRecord) {
		this.issueRecord = issueRecord;
	}
	public int getResult() {
		return result;
	}
	public void setResult(int result) {
		this.result = result;
	}
	public int getIsSend() {
		return isSend;
	}
	public void setIsSend(int isSend) {
		this.isSend = isSend;
	}
	public String getDateCreate() {
		return dateCreate;
	}
	public void setDateCreate(String dateCreate) {
		this.dateCreate = dateCreate;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	
	
	

}
