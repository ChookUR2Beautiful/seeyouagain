package com.xmniao.xmn.core.user_terminal.entity;

import java.util.Date;

import com.xmniao.xmn.core.base.BaseEntity;
/**
 * 商家分类关联类
 * @author ch
 *
 */
public class TSellerTrader extends BaseEntity{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	
	private String id;//编号
	private String sellerid;//商家编号
	private String category;//一级类别编号
	private String categoryName;//一级类别名称
	private String genre;//二级类别编号
	private String genreName;//二级类别名称
	private String creator;//创建人
	private Date dateCreated;//创建时间
	private String updator;//修改人
	private Date dateUpdated;//修改时间
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSellerid() {
		return sellerid;
	}
	public void setSellerid(String sellerid) {
		this.sellerid = sellerid;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getCategoryName() {
		return categoryName;
	}
	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public String getGenreName() {
		return genreName;
	}
	public void setGenreName(String genreName) {
		this.genreName = genreName;
	}
	public String getCreator() {
		return creator;
	}
	public void setCreator(String creator) {
		this.creator = creator;
	}
	public Date getDateCreated() {
		return dateCreated;
	}
	public void setDateCreated(Date dateCreated) {
		this.dateCreated = dateCreated;
	}
	public String getUpdator() {
		return updator;
	}
	public void setUpdator(String updator) {
		this.updator = updator;
	}
	public Date getDateUpdated() {
		return dateUpdated;
	}
	public void setDateUpdated(Date dateUpdated) {
		this.dateUpdated = dateUpdated;
	}
	@Override
	public String toString() {
		return "TSellerTrader [id=" + id + ", sellerid=" + sellerid
				+ ", category=" + category + ", categoryName=" + categoryName
				+ ", genre=" + genre + ", genreName=" + genreName
				+ ", creator=" + creator + ", dateCreated=" + dateCreated
				+ ", updator=" + updator + ", dateUpdated=" + dateUpdated + "]";
	}
	
	
}
