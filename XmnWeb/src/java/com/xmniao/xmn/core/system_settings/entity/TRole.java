public class TRole extends BaseEntity {

	private static final long serialVersionUID = -5544367822213554320L;
	private Long roleId;// ID
	private String roleName;// 名称
	private Integer isSystem;// 是否内置  0 否 1 是
	private String description;// 描述

	/**
	 * 创建一个新的实例 TRole.
	 * 
	 */
	public TRole() {
		super();
	}

	public TRole(Long roleId) {
		this.roleId = roleId;
	}

	/**
	 * roleId
	 * 
	 * @return the roleId
	 */
	public Long getRoleId() {
		return roleId;
	}

	/**
	 * @param roleId
	 *            the roleId to set
	 */
	public void setRoleId(Long roleId) {
		this.roleId = roleId;
	}

	/**
	 * roleName
	 * 
	 * @return the roleName
	 */
	public String getRoleName() {
		return roleName;
	}

	/**
	 * @param roleName
	 *            the roleName to set
	 */
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	/**
	 * isSystem
	 * 
	 * @return the isSystem
	 */
	public Integer getIsSystem() {
		return isSystem;
	}

	/**
	 * @param isSystem
	 *            the isSystem to set
	 */
	public void setIsSystem(Integer isSystem) {
		this.isSystem = isSystem;
	}

	/**
	 * description
	 * 
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}

	/**
	 * @param description
	 *            the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "TRole [roleId=" + roleId + ", roleName=" + roleName + ", isSystem=" + isSystem + ", description=" + description + ", ]";
	}
}
