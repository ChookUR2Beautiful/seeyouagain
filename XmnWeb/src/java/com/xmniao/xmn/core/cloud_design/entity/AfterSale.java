public class AfterSale extends BaseEntity{

	/**
	 * 
	 */
	private static final long serialVersionUID = -4638309207322634281L;
	
	private Long id;
	
	private String orderNo;
	
	private Integer state;

	private Integer dealType;
	
	private BigDecimal amount;
	
	private String plan;
	
	private Date createTime;
	
	private Date updateTime;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public Integer getState() {
		return state;
	}

	public void setState(Integer state) {
		this.state = state;
	}

	public Integer getDealType() {
		return dealType;
	}

	public void setDealType(Integer dealType) {
		this.dealType = dealType;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public String getPlan() {
		return plan;
	}

	public void setPlan(String plan) {
		this.plan = plan;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	@Override
	public String toString() {
		return "AfterSale [id=" + id + ", orderNo=" + orderNo + ", state="
				+ state + ", dealType=" + dealType + ", amount=" + amount
				+ ", plan=" + plan + ", createTime=" + createTime
				+ ", updateTime=" + updateTime + "]";
	}

	
 }
