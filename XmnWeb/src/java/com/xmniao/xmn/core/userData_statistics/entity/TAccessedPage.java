package com.xmniao.xmn.core.userData_statistics.entity;

import java.util.Date;

import com.xmniao.xmn.core.base.BaseEntity;

public class TAccessedPage extends BaseEntity {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2740661559257865137L;
	
	private String pageName;
	private Long visitedNum;
	private Date onSiteTime;
	/**
	 * 查询条件 字段无关
	 */
	private Date startCensusDate;//  统计开始日期
	private Date endCensusDate;//  统计结束日期
	
	public Date getStartCensusDate() {
		return startCensusDate;
	}
	public void setStartCensusDate(Date startCensusDate) {
		this.startCensusDate = startCensusDate;
	}
	public Date getEndCensusDate() {
		return endCensusDate;
	}
	public void setEndCensusDate(Date endCensusDate) {
		this.endCensusDate = endCensusDate;
	}
	public String getPageName() {
		return pageName;
	}
	public void setPageName(String pageName) {
		this.pageName = pageName;
	}
	public Long getVisitedNum() {
		return visitedNum;
	}
	public void setVisitedNum(Long visitedNum) {
		this.visitedNum = visitedNum;
	}
	public Date getOnSiteTime() {
		return onSiteTime;
	}
	public void setOnSiteTime(Date onSiteTime) {
		this.onSiteTime = onSiteTime;
	}
	@Override
	public String toString() {
		return "TAccessedPage [pageName=" + pageName + ", visitedNum="
				+ visitedNum + ", onSiteTime=" + onSiteTime + "]";
	}
	
	
}
