package com.xmniao.xmn.core.marketingmanagement.entity;
import com.xmniao.xmn.core.base.BaseEntity;
/**
 *@ClassName:TIntegralRule
 *@Description:积分活动规则表实体类
 *@author hls
 *@date:2016年3月4日下午4:10:03
 */
public class TIntegralRule extends BaseEntity {
	/**
	 * 
	 */
	private static final long serialVersionUID = -8587713908228119345L;
	private Integer rid;
	private Integer aid;
	private String grade;//商家类别
	private String startLadder;//起始阶梯
	private String endLadder;//结束阶梯
	private String rdate;
	private String udate;
	private int isRelationSellerNum;//参与活动商家总数
	
	
	public int getIsRelationSellerNum() {
		return isRelationSellerNum;
	}
	public void setIsRelationSellerNum(int isRelationSellerNum) {
		this.isRelationSellerNum = isRelationSellerNum;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public Integer getRid() {
		return rid;
	}
	public void setRid(Integer rid) {
		this.rid = rid;
	}
	public Integer getAid() {
		return aid;
	}
	public void setAid(Integer aid) {
		this.aid = aid;
	}
	public String getStartLadder() {
		return startLadder;
	}
	public void setStartLadder(String startLadder) {
		this.startLadder = startLadder;
	}
	
	public String getEndLadder() {
		return endLadder;
	}
	public void setEndLadder(String endLadder) {
		this.endLadder = endLadder;
	}
	public String getRdate() {
		return rdate;
	}
	public void setRdate(String rdate) {
		this.rdate = rdate;
	}
	public String getUdate() {
		return udate;
	}
	public void setUdate(String udate) {
		this.udate = udate;
	}
	@Override
	public String toString() {
		return "TIntegralRule [rid=" + rid + ", aid=" + aid + ", grade="
				+ grade + ", startLadder=" + startLadder + ", endLadder="
				+ endLadder + ", rdate=" + rdate + ", udate=" + udate
				+ ", isRelationSellerNum=" + isRelationSellerNum + "]";
	}
	
}