package com.xmniao.xmn.core.coupon.response;

import java.io.Serializable;

@SuppressWarnings("serial")
public class ApplyStoresResponse implements Serializable{

	private String sellerId;//店铺ID
	
	private String sellerName;//店铺名称
	
	private String zoneName;//商圈名称
	
	private String tradeName;//二级分类名称
	
	private String sellerImage;//店铺图片
	
	private String sellerConsums;//店铺消费人次
	
	private String zhiboType;//直播类型;

	public String getSellerId() {
		return sellerId;
	}

	public void setSellerId(String sellerId) {
		this.sellerId = sellerId;
	}

	public String getSellerName() {
		return sellerName;
	}

	public void setSellerName(String sellerName) {
		this.sellerName = sellerName;
	}

	public String getZoneName() {
		return zoneName;
	}

	public void setZoneName(String zoneName) {
		this.zoneName = zoneName;
	}

	public String getTradeName() {
		return tradeName;
	}

	public void setTradeName(String tradeName) {
		this.tradeName = tradeName;
	}

	public String getSellerImage() {
		return sellerImage;
	}

	public void setSellerImage(String sellerImage) {
		this.sellerImage = sellerImage;
	}

	public String getSellerConsums() {
		return sellerConsums;
	}

	public void setSellerConsums(String sellerConsums) {
		this.sellerConsums = sellerConsums;
	}
	
	public String getZhiboType() {
		return zhiboType;
	}

	public void setZhiboType(String zhiboType) {
		this.zhiboType = zhiboType;
	}

	@Override
	public String toString() {
		return "ApplyStoresResponse [sellerId=" + sellerId + ", sellerName=" + sellerName + ", zoneName=" + zoneName
			+ ", tradeName=" + tradeName + ", sellerImage=" + sellerImage + ", sellerConsums=" + sellerConsums + ", zhiboType="
			+ zhiboType + "]";
	}
	
}
