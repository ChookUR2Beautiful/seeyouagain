package com.xmniao.xmn.core.live.entity;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;


/**
 * 直播礼物信息类
 * @author yhl
 * 2016年8月10日17:23:49
 * */
public class LiveGiftsInfo implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 6650464273838935486L;
	
	// 直播礼物id
	private Integer id ;
	
	//礼物名称
	private String gift_name;
	
	//  礼物类型 
	private Integer gift_type;
	
	//礼物单价
	private BigDecimal gift_price;
	
	//礼物代表的经验值
	private Integer gift_experience;
	
	//礼物头像
	private String gift_avatar;
	
	//创建时间
	private Date create_time;
	
	//更新时间
	private Date  update_time;
	
	//是否参与免费赠送活动  1 是  0 否 
	private int free_status;
	
	//状态
	private int status;
	
	//是否关联大礼包
	private int is_gift_bag;
	
	//关联大礼包
	private int gift_bag_id;
	
	//直播用户ID
	private Long liver_id;
		
	//获取礼物个数
	private int gift_nums;
	
	//是否连击
	private String isSeries;
	
	//是否广播
	private int isRadio;
	
	//礼物是否返利鸟币  0 默认返利 1 不返利
	private Integer backCoinStatus = 0;
	
	//礼物发送来源 0 直播 ,1 套餐, 2 商城
	private Integer giftSource = 0;
	
	//商城礼物,套餐礼物对应的实际商品ID
	private Integer saleProductId;
	
	//运费
	private BigDecimal freight;
	
	//礼物的优先等级
	private String msgPriority;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getGift_name() {
		return gift_name;
	}

	public void setGift_name(String gift_name) {
		this.gift_name = gift_name;
	}

	public Integer getGift_type() {
		return gift_type;
	}

	public void setGift_type(Integer gift_type) {
		this.gift_type = gift_type;
	}

	public BigDecimal getGift_price() {
		return gift_price;
	}

	public void setGift_price(BigDecimal gift_price) {
		this.gift_price = gift_price;
	}

	public Integer getGift_experience() {
		return gift_experience;
	}

	public void setGift_experience(Integer gift_experience) {
		this.gift_experience = gift_experience;
	}

	public String getGift_avatar() {
		return gift_avatar;
	}

	public void setGift_avatar(String gift_avatar) {
		this.gift_avatar = gift_avatar;
	}

	public Date getCreate_time() {
		return create_time;
	}

	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}

	public Date getUpdate_time() {
		return update_time;
	}

	public void setUpdate_time(Date update_time) {
		this.update_time = update_time;
	}

	public int getFree_status() {
		return free_status;
	}

	public void setFree_status(int free_status) {
		this.free_status = free_status;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getIs_gift_bag() {
		return is_gift_bag;
	}

	public void setIs_gift_bag(int is_gift_bag) {
		this.is_gift_bag = is_gift_bag;
	}

	public int getGift_bag_id() {
		return gift_bag_id;
	}

	public void setGift_bag_id(int gift_bag_id) {
		this.gift_bag_id = gift_bag_id;
	}

	public Long getLiver_id() {
		return liver_id;
	}

	public void setLiver_id(Long liver_id) {
		this.liver_id = liver_id;
	}

	public int getGift_nums() {
		return gift_nums;
	}

	public void setGift_nums(int gift_nums) {
		this.gift_nums = gift_nums;
	}

	public String getIsSeries() {
		return isSeries;
	}

	public void setIsSeries(String isSeries) {
		this.isSeries = isSeries;
	}

	public int getIsRadio() {
		return isRadio;
	}

	public void setIsRadio(int isRadio) {
		this.isRadio = isRadio;
	}

	public Integer getBackCoinStatus() {
		return backCoinStatus;
	}

	public void setBackCoinStatus(Integer backCoinStatus) {
		this.backCoinStatus = backCoinStatus;
	}

	public Integer getGiftSource() {
		return giftSource;
	}

	public void setGiftSource(Integer giftSource) {
		this.giftSource = giftSource;
	}

	public Integer getSaleProductId() {
		return saleProductId;
	}

	public void setSaleProductId(Integer saleProductId) {
		this.saleProductId = saleProductId;
	}

	public BigDecimal getFreight() {
		return freight;
	}

	public void setFreight(BigDecimal freight) {
		this.freight = freight;
	}

	public String getMsgPriority() {
		return msgPriority;
	}

	public void setMsgPriority(String msgPriority) {
		this.msgPriority = msgPriority;
	}

	@Override
	public String toString() {
		return "LiveGiftsInfo [id=" + id + ", gift_name=" + gift_name
				+ ", gift_type=" + gift_type + ", gift_price=" + gift_price
				+ ", gift_experience=" + gift_experience + ", gift_avatar="
				+ gift_avatar + ", create_time=" + create_time
				+ ", update_time=" + update_time + ", free_status="
				+ free_status + ", status=" + status + ", is_gift_bag="
				+ is_gift_bag + ", gift_bag_id=" + gift_bag_id + ", liver_id="
				+ liver_id + ", gift_nums=" + gift_nums + ", isSeries="
				+ isSeries + ", isRadio=" + isRadio + ", backCoinStatus="
				+ backCoinStatus + ", giftSource=" + giftSource
				+ ", saleProductId=" + saleProductId + ", freight=" + freight
				+ ", msgPriority=" + msgPriority + "]";
	}


}
