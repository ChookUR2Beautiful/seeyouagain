package com.xmniao.xmn.core.live.entity;

import java.io.Serializable;
import java.util.Date;

/**
 * 描述  用户观看直播记录实体类
 * @author yhl
 * 2016年8月15日15:36:57
 * */
public class LiveViewRecordInfo implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -4151805221720223624L;
	
	//用户观看直播记录id
	private Long id ;
	// 直播用户ID
	private Long liver_id;
	// 寻蜜鸟会员id
	private Long uid ;
	// 用户当前等级数
	private Integer rank_no;
	// 用户当前等级头衔名称
	private String achievement;
	// 直播用户类型： 1 主播 2 普通用户
	private Long liver_utype;
	// 主播用户id
	private Long anchor_id;
	// 主播房间编号
	private Long anchor_room_no;
	// 直播记录id
	private Long live_record_id;
	// 直播类型  1 直播  0 回放
	private Integer live_type;
	// 观看状态  1 正在观看  0 已退出 2直播结束
	private Integer live_status;
	//直播观看开始时间
	private Date start_time ;
	// 直播观看结束时间
	private Date end_time;
	// 直播连续观看时长
	private Integer view_duration;
	// 创建时间
	private Date create_time;
	//更新时间
	private Date update_time ;
	//主播结束直播时间
	private Date room_end_time;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getLiver_id() {
		return liver_id;
	}
	public void setLiver_id(Long liver_id) {
		this.liver_id = liver_id;
	}
	public Long getUid() {
		return uid;
	}
	public void setUid(Long uid) {
		this.uid = uid;
	}
	public Integer getRank_no() {
		return rank_no;
	}
	public void setRank_no(Integer rank_no) {
		this.rank_no = rank_no;
	}
	public String getAchievement() {
		return achievement;
	}
	public void setAchievement(String achievement) {
		this.achievement = achievement;
	}
	public Long getLiver_utype() {
		return liver_utype;
	}
	public void setLiver_utype(Long liver_utype) {
		this.liver_utype = liver_utype;
	}
	public Long getAnchor_id() {
		return anchor_id;
	}
	public void setAnchor_id(Long anchor_id) {
		this.anchor_id = anchor_id;
	}
	public Long getAnchor_room_no() {
		return anchor_room_no;
	}
	public void setAnchor_room_no(Long anchor_room_no) {
		this.anchor_room_no = anchor_room_no;
	}
	public Long getLive_record_id() {
		return live_record_id;
	}
	public void setLive_record_id(Long live_record_id) {
		this.live_record_id = live_record_id;
	}
	public Integer getLive_type() {
		return live_type;
	}
	public void setLive_type(Integer live_type) {
		this.live_type = live_type;
	}
	public Integer getLive_status() {
		return live_status;
	}
	public void setLive_status(Integer live_status) {
		this.live_status = live_status;
	}
	public Date getStart_time() {
		return start_time;
	}
	public void setStart_time(Date start_time) {
		this.start_time = start_time;
	}
	public Date getEnd_time() {
		return end_time;
	}
	public void setEnd_time(Date end_time) {
		this.end_time = end_time;
	}
	public Integer getView_duration() {
		return view_duration;
	}
	public void setView_duration(Integer view_duration) {
		this.view_duration = view_duration;
	}
	public Date getCreate_time() {
		return create_time;
	}
	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}
	public Date getUpdate_time() {
		return update_time;
	}
	public void setUpdate_time(Date update_time) {
		this.update_time = update_time;
	}
	public Date getRoom_end_time() {
		return room_end_time;
	}
	public void setRoom_end_time(Date room_end_time) {
		this.room_end_time = room_end_time;
	}
	
	@Override
	public String toString() {
		return "LiveViewRecordInfo [id=" + id + ", liver_id=" + liver_id
				+ ", uid=" + uid + ", rank_no=" + rank_no + ", achievement="
				+ achievement + ", liver_utype=" + liver_utype + ", anchor_id="
				+ anchor_id + ", anchor_room_no=" + anchor_room_no
				+ ", live_record_id=" + live_record_id + ", live_type="
				+ live_type + ", live_status=" + live_status + ", start_time="
				+ start_time + ", end_time=" + end_time + ", view_duration="
				+ view_duration + ", create_time=" + create_time
				+ ", update_time=" + update_time + ", room_end_time="
				+ room_end_time + "]";
	}
	

}
