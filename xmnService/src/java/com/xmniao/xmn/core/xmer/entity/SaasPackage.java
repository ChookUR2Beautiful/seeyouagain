package com.xmniao.xmn.core.xmer.entity;

import java.util.Date;

/**
 *@ClassName:SaasPackage
 *@Description:SAAS套餐表实体
 *@author hls
 *@date:2016年5月19日下午3:11:27
 */
public class SaasPackage {
	

	private Integer id;//SAAS套餐id
	private Integer nums;//约定套数
	private Double price;//押金金额
	private Integer soldNums;//已销售数量
	private Integer returnNums;//已退套参数
	private Integer  status;//数据状态默认1 1正常  0已删除 2 已下架
	private Date updateDate;//更新时间
	private Double agio; //折扣
	
	public Double getAgio() {
		return agio;
	}

	public void setAgio(Double agio) {
		this.agio = agio;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getNums() {
		return nums;
	}

	public void setNums(Integer nums) {
		this.nums = nums;
	}

	public Double getPrice() {
		return price;
	}

	public void setPrice(Double price) {
		this.price = price;
	}

	public Integer getSoldNums() {
		return soldNums;
	}

	public void setSoldNums(Integer soldNums) {
		this.soldNums = soldNums;
	}

	public Integer getReturnNums() {
		return returnNums;
	}

	public void setReturnNums(Integer returnNums) {
		this.returnNums = returnNums;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Date getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}

	@Override
	public String toString() {
		return "SaasPackage [id=" + id + ", nums=" + nums + ", price=" + price
				+ ", soldNums=" + soldNums + ", returnNums=" + returnNums
				+ ", status=" + status + ", updateDate=" + updateDate
				+ ", agio=" + agio + "]";
	}

}
