package com.xmniao.xmn.core.catehome.response;

/**
 * 
* @projectName: xmnService 
* @ClassName: HomeImageResponse    
* @Description:首页图片返回结果集   
* @author: liuzhihao   
* @date: 2017年2月22日 下午5:00:12
 */
public class HomeImageResponse {

	private Integer id;//关联ID
	
	private String picUrl;//图片
	
	private Integer sort;//图片排序
	
	private String content;//图片内容
	
	private String logRequired;//是否登录才可查看
	
	private String	type;//图片跳转类型


	private String liveStartType = "";  //开播类型 0通告开播  1自定义开播
	private String groupId = "";  //群组号
	private String anchorRoomNo = "";  //房间编号



	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getPicUrl() {
		return picUrl;
	}

	public void setPicUrl(String picUrl) {
		this.picUrl = picUrl;
	}

	public Integer getSort() {
		return sort;
	}

	public void setSort(Integer sort) {
		this.sort = sort;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getLogRequired() {
		return logRequired;
	}

	public void setLogRequired(String logRequired) {
		this.logRequired = logRequired;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getLiveStartType() {
		return liveStartType;
	}

	public void setLiveStartType(String liveStartType) {
		this.liveStartType = liveStartType;
	}

	public String getGroupId() {
		return groupId;
	}

	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}

	public String getAnchorRoomNo() {
		return anchorRoomNo;
	}

	public void setAnchorRoomNo(String anchorRoomNo) {
		this.anchorRoomNo = anchorRoomNo;
	}


	@Override
	public String toString() {
		return "HomeImageResponse{" +
				"id=" + id +
				", picUrl='" + picUrl + '\'' +
				", sort=" + sort +
				", content='" + content + '\'' +
				", logRequired='" + logRequired + '\'' +
				", type='" + type + '\'' +
				", liveStartType='" + liveStartType + '\'' +
				", groupId='" + groupId + '\'' +
				", anchorRoomNo='" + anchorRoomNo + '\'' +
				'}';
	}
}
