package com.xmniao.xmn.core.order.entity;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;

/**
 * 
* @Description:体验卡返回参数相关类
* @author: yhl 
* @date 2017年5月9日 下午5:16:13 
*
 */
public class ExperienceofficerConfig implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -2719734792044121190L;
	//ID
	private Integer id;
	//体验卡标题
	private String title;
	//体验卡类型
	private Integer type;
	//是否免费获取 0 否 1 是
	private Integer isFree;
	//销售价格
	private BigDecimal price;
	//体验次数
	private Integer nums;
	//有效天数
	private Integer days;
	//修改时间
	private Date updateTime;
	//过期时间
	private String outDate;
	
	//一下为钱包参数
	//钱包余额
	private BigDecimal balanceMoney;
	//鸟币余额
	private BigDecimal balanceCoin;
	//订单金额
	private BigDecimal amount;
	
	
	//订单相关参数
	private String orderNo;
	
	
	public String getOrderNo() {
		return orderNo;
	}

	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Integer getIsFree() {
		return isFree;
	}

	public void setIsFree(Integer isFree) {
		this.isFree = isFree;
	}


	public Integer getNums() {
		return nums;
	}

	public void setNums(Integer nums) {
		this.nums = nums;
	}

	public Integer getDays() {
		return days;
	}

	public void setDays(Integer days) {
		this.days = days;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public BigDecimal getPrice() {
		return price;
	}

	public void setPrice(BigDecimal price) {
		this.price = price;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	public BigDecimal getBalanceMoney() {
		return balanceMoney;
	}

	public void setBalanceMoney(BigDecimal balanceMoney) {
		this.balanceMoney = balanceMoney;
	}

	public BigDecimal getBalanceCoin() {
		return balanceCoin;
	}

	public void setBalanceCoin(BigDecimal balanceCoin) {
		this.balanceCoin = balanceCoin;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public String getOutDate() {
		return outDate;
	}

	public void setOutDate(String outDate) {
		this.outDate = outDate;
	}

	@Override
	public String toString() {
		return "ExperienceofficerConfig [id=" + id + ", title=" + title
				+ ", type=" + type + ", isFree=" + isFree + ", price=" + price
				+ ", nums=" + nums + ", days=" + days + ", updateTime="
				+ updateTime + ", outDate=" + outDate + ", balanceMoney="
				+ balanceMoney + ", balanceCoin=" + balanceCoin + ", amount="
				+ amount + ", orderNo=" + orderNo + "]";
	}
	
}
