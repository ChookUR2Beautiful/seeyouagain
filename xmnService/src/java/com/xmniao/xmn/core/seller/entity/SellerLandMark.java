package com.xmniao.xmn.core.seller.entity;

public class SellerLandMark {
	
	private int lid;
	
	private int sellerid;
	
	private double longitude;
	
	private double latitude;
	
	private double ggLat;
	
	private double ggLng;
	
	private long range ;
	

	public long getRange() {
		return range;
	}

	public void setRange(long range) {
		this.range = range;
	}

	public int getLid() {
		return lid;
	}

	public void setLid(int lid) {
		this.lid = lid;
	}

	public int getSellerid() {
		return sellerid;
	}

	public void setSellerid(int sellerid) {
		this.sellerid = sellerid;
	}

	public double getLongitude() {
		return longitude;
	}

	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}

	public double getLatitude() {
		return latitude;
	}

	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}

	public double getGgLat() {
		return ggLat;
	}

	public void setGgLat(double ggLat) {
		this.ggLat = ggLat;
	}

	public double getGgLng() {
		return ggLng;
	}

	public void setGgLng(double ggLng) {
		this.ggLng = ggLng;
	}

	
	

}
