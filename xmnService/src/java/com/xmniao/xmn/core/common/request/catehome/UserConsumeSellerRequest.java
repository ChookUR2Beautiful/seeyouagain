package com.xmniao.xmn.core.common.request.catehome;

import com.xmniao.xmn.core.base.BaseRequest;

/**
 * 
* @projectName: xmnService 
* @ClassName: XmnHomeRequest    
* @Description: 寻蜜鸟首页请求参数   
* @author: liuzhihao   
* @date: 2016年11月10日 下午3:21:35
 */
public class UserConsumeSellerRequest extends BaseRequest{
	
	private static final long serialVersionUID = 1L;

	//经度
	private Double longitude = 0D;
	
	//纬度
	private Double latitude = 0D;
	
	//纬度
	private Integer page = 1;

	public Double getLongitude() {
		return longitude;
	}

	public void setLongitude(Double longitude) {
		this.longitude = longitude;
	}

	public Double getLatitude() {
		return latitude;
	}

	public void setLatitude(Double latitude) {
		this.latitude = latitude;
	}

	public Integer getPage() {
		return page;
	}

	public void setPage(Integer page) {
		this.page = page;
	}

	@Override
	public String toString() {
		return "UserConsumeSellerRequest [longitude=" + longitude + ", latitude=" + latitude + ", page=" + page + "]";
	}

	
}
