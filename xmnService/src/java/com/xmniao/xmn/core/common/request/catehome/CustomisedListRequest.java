package com.xmniao.xmn.core.common.request.catehome;

import com.xmniao.xmn.core.base.BaseRequest;

@SuppressWarnings("serial")
public class CustomisedListRequest extends BaseRequest{

	private Double minprice;//最低价格
	
	private Double maxprice;//最高价格
	
	private Integer genre;//分类id
	
	private Integer zoneid;//商圈id
	
	private Integer eattype;//吃货类型
	
	private Integer page;
	
	private Integer pageSize=8;
	
	private Double lon;
	
	private Double lat;

	public Double getMinprice() {
		return minprice;
	}

	public void setMinprice(Double minprice) {
		this.minprice = minprice;
	}

	public Double getMaxprice() {
		return maxprice;
	}

	public void setMaxprice(Double maxprice) {
		this.maxprice = maxprice;
	}

	public Integer getGenre() {
		return genre;
	}

	public void setGenre(Integer genre) {
		this.genre = genre;
	}

	public Integer getZoneid() {
		return zoneid;
	}

	public void setZoneid(Integer zoneid) {
		this.zoneid = zoneid;
	}

	public Integer getEattype() {
		return eattype;
	}

	public void setEattype(Integer eattype) {
		this.eattype = eattype;
	}

	public Integer getPage() {
		return page;
	}

	public void setPage(Integer page) {
		this.page = page;
	}

	public Integer getPageSize() {
		return pageSize;
	}

	public void setPageSize(Integer pageSize) {
		this.pageSize = pageSize;
	}

	public Double getLon() {
		return lon;
	}

	public void setLon(Double lon) {
		this.lon = lon;
	}

	public Double getLat() {
		return lat;
	}

	public void setLat(Double lat) {
		this.lat = lat;
	}
	
}
