package com.xmniao.xmn.core.common.request.live;

import java.io.Serializable;

/**
 * 
*    
* 项目名称：xmnService   
* 类名称：LiveHomeRequest   
* 类描述：   直播首页(banner图,预告/预售,直播)请求参数
* 创建人：yezhiyong   
* 创建时间：2016年12月1日 下午3:25:48   
* @version    
*
 */
public class LiveHomeRequest implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -4610563493947987284L;
	
	/**
	 * 会话令牌
	 */
	private String sessiontoken;
	
	/**
	 * 用户经度
	 */
	private Double longitude;
	
	/**
	 * 用户纬度
	 */
	private Double latitude;

	public String getSessiontoken() {
		return sessiontoken;
	}

	public void setSessiontoken(String sessiontoken) {
		this.sessiontoken = sessiontoken;
	}

	public Double getLongitude() {
		return longitude;
	}

	public void setLongitude(Double longitude) {
		this.longitude = longitude;
	}

	public Double getLatitude() {
		return latitude;
	}

	public void setLatitude(Double latitude) {
		this.latitude = latitude;
	}

	@Override
	public String toString() {
		return "LiveHomeRequest [sessiontoken=" + sessiontoken + ", longitude="
				+ longitude + ", latitude=" + latitude + "]";
	}
	
}
